package sk.stuba.fiit.enums;

/**
 * Enum representing the different rarities of waves in the game.
 * This enum helps classify waves based on their difficulty or rarity.
 */
public enum WaveRarity {
    /**
     * Represents the most difficult and rare waves, typically featuring the strongest enemies or challenges.
     */
    LEGENDARY,

    /**
     * Represents waves with high difficulty and valuable rewards, but not as rare as legendary.
     */
    EPIC,

    /**
     * Represents moderately difficult waves with decent rewards.
     */
    RARE,

    /**
     * Represents waves with a relatively low difficulty level and moderate rewards.
     */
    UNCOMMON,

    /**
     * Represents the easiest waves with the least difficulty and basic rewards.
     */
    COMMON
}
