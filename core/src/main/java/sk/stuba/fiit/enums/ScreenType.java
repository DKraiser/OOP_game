package sk.stuba.fiit.enums;

/**
 * Enum representing the different types of screens in the game.
 * The screens determine what part of the game the player is interacting with.
 */
public enum ScreenType {
    /**
     * Represents the main game screen where the player engages in the core gameplay.
     */
    GAME,

    /**
     * Represents the shop screen where the player can purchase upgrades or items.
     */
    SHOP,

    /**
     * Represents the restart screen, which is displayed when the player chooses to restart the game.
     */
    RESTART
}
